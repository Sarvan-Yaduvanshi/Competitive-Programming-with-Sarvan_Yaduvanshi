inline i64 modpow(i64 base, i64 exp, i64 mod = MOD) {
    i64 res = 1;
    base %= mod;
    while (exp > 0) {
        if (exp & 1) res = (res * base) % mod;
        base = (base * base) % mod;
        exp >>= 1;
    }
    return res;
}

/* Problem: Leetcode #1456 - Maximum Number of Vowels in a Substring of Given Length
 * Given a string s and an integer k, return the maximum number of vowel letters in any substring of s with length k.
 * Vowel letters in English are 'a', 'e', 'i', 'o', and 'u'.
 * Example 1:
 * Input: s = "abciiidef", k = 3
 * Output: 3
 * Explanation: The substring "iii" contains 3 vowel letters.
 * Example 2:
 * Input: s = "aeiou", k = 2
 * Output: 2
 * Explanation: Any substring of length 2 contains 2 vowels.
 * Example 3:
 * Input: s = "leetcode", k = 3
 * Output: 2
 * Explanation: "lee", "eet" and "ode" contain 2 vowels.
 * Constraints:
 * 1 <= s.length <= 10^5
 * s consists of lowercase English letters.
 * 1 <= k <= s.length
 *
 * Approach:
 *          1. Use a sliding window of size k to traverse the string.
 *          2. Maintain a count of vowels in the current window.
 *          3. Update the maximum count whenever the window moves.
 *          4. Time Complexity: O(N), Space Complexity: O(1)
 */

int maxVowels(const string& s, int k){
    int currVowels = 0;
    int maxVowels = 0;

    auto isVowel = [](const char ch){
        return ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u';
    };

    for (int i = 0; i < s.length(); i++){
        if (isVowel(s[i]))
            currVowels++;

        if (i >= k){
            if (isVowel(s[i - k]))
                currVowels--;
        }

        if (i >= k - 1)
            maxVowels = max(maxVowels, currVowels);
    }
    return maxVowels;
}

